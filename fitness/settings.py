import os
from pathlib import Path

from dotenv import load_dotenv

# Directory containing `manage.py` (project root)
BASE_DIR = Path(__file__).resolve().parent.parent

def _is_railway_runtime() -> bool:
    return any(
        os.getenv(var)
        for var in (
            "RAILWAY_ENVIRONMENT_NAME",
            "RAILWAY_ENVIRONMENT_ID",
            "RAILWAY_PROJECT_ID",
            "RAILWAY_SERVICE_ID",
            "RAILWAY_SERVICE_NAME",
            "RAILWAY_PUBLIC_DOMAIN",
        )
    )


# Local-only convenience: load `.env` while running locally.
# In production (Railway), rely on Railway Variables only.
if not _is_railway_runtime():
    load_dotenv(BASE_DIR / ".env")


default_env = "production" if _is_railway_runtime() else "local"
DJANGO_ENV = os.getenv("DJANGO_ENV", default_env).lower()
if _is_railway_runtime() and DJANGO_ENV != "production":
    # Railway deploys should always use production settings.
    DJANGO_ENV = "production"

# Keep the Django settings entrypoint small and delegate to environment-specific modules.
if DJANGO_ENV == "production":
    from .config.production import *  # noqa: F403,F401
else:
    from .config.local import *  # noqa: F403,F401
